function extractMenuData(data) {
  const result = {};

  function traverse(node, currentSection) {
    if (node.tagName === "H3") {
      currentSection = node.textContent.trim();
      result[currentSection.toLowerCase().replace(/ /g, "_")] = { lst: [] };
    } else if (node.tagName === "DIV" && node.classList.contains("css-1rynq56")) {
      const item = node.textContent.trim();
      const price = node.nextSibling.textContent.trim();
      const info = node.nextSibling.nextSibling?.textContent?.trim();

      result[currentSection].lst.push({
        item,
        price,
        ...(info && { info }),
      });
    }

    node.children.forEach((child) => traverse(child, currentSection));
  }

  traverse(data[0], null);

  return result;
}

